/**
 * Returns the result of incrementing the supplied number by 1
 * @param {number} n
 * @returns {number}
 */
const inc = n => n + 1

/**
 * Returns the result of doubling the supplied number
 * @param {number} n
 * @returns {number}
 */
const dbl = n => n * 2

/**
 *
 * @param {string} s
 */
const toUpper = s => s.toUpperCase()

module.exports = {
  dbl,
  inc,
  toUpper
}
